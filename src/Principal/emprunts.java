    void tableEmprunts() {
         String[] colonne={"ID-Emprunt","ID-Livre","ID-utilisateur","Date-Emprunte","NbLivre-Emprunté","Date-Retour"};
        String afficher[]= new String[6];
        DefaultTableModel model = new DefaultTableModel(null,colonne);
        try {
            connect();
            Statement st =con.createStatement();
            rs=st.executeQuery("select * from emprunter");
            
            while(rs.next()){
                afficher[0]=rs.getString("IDEmprunt");
                afficher[1]=rs.getString("IDLivre");
                afficher[2]=rs.getString("IDUtilisateur");
                afficher[3]=rs.getString("DateEmprunte");
                afficher[4]=rs.getString("NbLivreEmprunte");
                 afficher[5]=rs.getString("DateRetour");
                model.addRow(afficher);
            }
            txttable.setModel(model);
            rs.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        } 
    }
